import numpy as np
from scipy.interpolate import spline
import matplotlib.pyplot as plt
plt.xlabel("Energy consumption", size=14)
plt.ylabel("CDF", size=14)
t =np.array([10,20,30,40,50,60,70,80,90,92,95])
t2=np.array([35,40,50,60,70,80,90,100,101,102])
Fine_1=np.array([0,0,0.02,0.09,0.26,0.57,0.87,0.98,1,1,1])
Rough_1=np.array([0,0,0.01,0.06,0.21,0.43,0.76,0.93,1,1,1])
Fine_2=np.array([0,0,0.03,0.09,0.41,0.80,0.95,1,1,1])
Rough_2=np.array([0,0,0.02,0.05,0.32,0.78,0.94,1,1,1])
Local=np.array([0,0,0.01,0.03,0.30,0.74,0.93,1,1,1])
x_new=np.linspace(t.min(),t.max(),300)
x_new2=np.linspace(t2.min(),t2.max(),300)
power_smooth = spline(t,Fine_1,x_new)
rough_smooth=spline(t,Rough_1,x_new)
fine_smooth=spline(t2,Fine_2,x_new2)
rough_smooth2=spline(t2,Rough_2,x_new2)
local_smooth=spline(t2,Local,x_new2)
plt.plot(x_new,power_smooth,color='blue',linewidth=2)
plt.plot(x_new,rough_smooth,color='red',linewidth=2)
plt.plot(x_new2,fine_smooth,color='green',linewidth=2)
plt.plot(x_new2,rough_smooth2,color='black',linewidth=2)
plt.plot(x_new2,local_smooth,color='brown',linewidth=2)
plt.legend(['Fine R=2000','Rough R=2000','Fine R=500','Rough R=500','Local'],loc='center center')
plt.show()